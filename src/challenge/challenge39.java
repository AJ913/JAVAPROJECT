package challenge;

import java.util.Scanner;

public class challenge39 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.println("Enter a number:");
        int n = sc.nextInt();
        pattern(n);
    }

    public static void pattern(int n) {
        int rows = 0;
        while (rows < n) {
            System.out.print("*");
            int i = 0;
            while (i < rows) {
                System.out.print(" *");
                i++;
            }
            System.out.println();
            rows++;
        }
    }
}
